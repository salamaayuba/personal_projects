def calculate(operation, a, b):
    if operation == 1:
        print("{} + {} = {}".format(a, b, a+b))

    elif operation == 2:
        print("{} - {} = {}".format(a, b, a-b))

    elif operation == 3:
        print("{} * {} = {}".format(a, b, a*b))

    elif operation == 4:
        print("{} / {} = {}".format(a, b, a/b))